letter = input("Введите букву латинского алфавита: ").strip().lower()

if letter in ['a', 'e', 'i', 'o', 'u']:
    print("Эта буква гласная.")
elif letter == 'y':
    print("Эта буква может быть как гласной, так и согласной.")
elif letter.isalpha() and len(letter) == 1:
    print("Эта буква согласная.")
else:
    print("Ошибка: введен некорректный символ. Пожалуйста, введите одну букву латинского алфавита.")